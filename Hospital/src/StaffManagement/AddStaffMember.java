public static String strnext(String s) {
   int l = s.length();
   int num;
        num = Integer.parseInt(s.substring(1, l));
 
   String result;
   result=("e"+(num+1));
   return result;
 }
public static String strnext2(String s) {
   int l = s.length();
   int num;
        num = Integer.parseInt(s.substring(2, l));
 
   String result;
   result=("ad"+(num+1));
   return result;
 }

