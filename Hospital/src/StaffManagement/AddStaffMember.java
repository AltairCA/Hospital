    private void cmbdesignationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbdesignationActionPerformed

        // TODO add your handling code here:
        String designation = (String) cmbdesignation.getSelectedItem();
        if ("Support Staff".equals(designation)) {
            cmbsupportstaff.setEnabled(true);

            txtfurther.setEnabled(false);
            txtspecial.setEnabled(false);
            txtgrade.setEnabled(false);
            txtfurther.setEditable(false);
            txtspecial.setEditable(false);
            txtgrade.setEditable(false);

        } else if ("Doctor".equals(designation)) {

            txtgrade.setEnabled(false);

            txtgrade.setEditable(false);
            txtfurther.setEditable(true);
            txtspecial.setEditable(true);
            txtfurther.setEnabled(true);
            txtspecial.setEnabled(true);

        } else if ("Nurse".equals(designation)) {
            txtfurther.setEnabled(false);
            txtspecial.setEnabled(false);

            txtfurther.setEditable(false);
            txtspecial.setEditable(false);

            txtgrade.setEditable(true);
            txtgrade.setEnabled(true);

        } else {
            cmbsupportstaff.setEnabled(false);
            txtfurther.setEnabled(false);
            txtspecial.setEnabled(false);
            txtgrade.setEnabled(false);
            txtfurther.setEditable(false);
            txtspecial.setEditable(false);
            txtgrade.setEditable(false);
        }

    }//GEN-LAST:event_cmbdesignationActionPerformed

